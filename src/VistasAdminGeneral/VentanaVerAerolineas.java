    private void actualizarTabla() {
        LSE<Aerolinea> aerolineas = controlador.obtenerAerolineas();

        for (int j = modelo.getRowCount() - 1; j >= 0; j--) {
            modelo.removeRow(j);
        }

        for (int i = 0; i < aerolineas.size(); i++) {
            Aerolinea aux = aerolineas.get(i);
            String adminInfo = "";
            String adminUsu = "";
            String adminContra = "";
            if (aux.getListaEmpleadosAerolinea().size() > 0) {
                AdministradorAerolinea admin = (AdministradorAerolinea) aux.getListaEmpleadosAerolinea().get(0);
                adminInfo = admin.getNombres() + " " + admin.getApellidos();
                adminUsu = admin.getCorreo();
                adminContra = admin.getContrasenia();
            }

            Object[] ob = {adminInfo, aux.getPais(), aux.getCodigoAerolinea(), aux.getNombreAerolinea(), adminUsu, adminContra};
            modelo.addRow(ob);
        }
    }

