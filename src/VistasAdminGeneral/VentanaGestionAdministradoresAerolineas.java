    private void btnBuscar2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscar2MouseClicked
        if(txtIdBuscar.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese el numero de identificación que desea buscar");
            return;
        }else {
            String identificacion = txtIdBuscar.getText();
            AdministradorAerolinea administradorAerolinea = (AdministradorAerolinea) controlador.buscarAdminAerolineaId(identificacion);
            Aerolinea aerolineaBuscada = controlador.buscarAdAerolinea(identificacion);
            if(administradorAerolinea != null) {
                txtNombres.setText(administradorAerolinea.getNombres());
                txtApellidos.setText(administradorAerolinea.getApellidos());
                txtEdad.setText(String.valueOf(administradorAerolinea.getEdad()));
                txtIdentificacion.setText(administradorAerolinea.getIdentificacion());
                cboGenero.setSelectedItem(administradorAerolinea.getGenero());
                cboAerolinea.setSelectedItem(aerolineaBuscada.getNombreAerolinea());
                txtCorreo.setText(administradorAerolinea.getCorreo());
                txtFechaRegistro.setText(administradorAerolinea.getFechaContratación().toString());
                txtContrasenia.setText(administradorAerolinea.getContrasenia());
                txtConfirmarContrasenia.setText(administradorAerolinea.getContrasenia());
                txtTelefono.setText(administradorAerolinea.getNumTelefono());
            } else {
                JOptionPane.showMessageDialog(null, "El Administrador de aerolinea con numero de identificación: " + identificacion + "\n no se encuentra registrado");
            }
        }
    }//GEN-LAST:event_btnBuscar2MouseClicked

