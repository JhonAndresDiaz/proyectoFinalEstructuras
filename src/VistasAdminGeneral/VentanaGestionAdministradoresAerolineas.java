    private void btnRegistrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRegistrarMouseClicked
        if(txtNombres.getText().isEmpty() || txtApellidos.getText().isEmpty() || txtIdentificacion.getText().isEmpty() || txtEdad.getText().isEmpty() || txtTelefono.getText().isEmpty() || cboGenero.getSelectedIndex() == 0 || cboAerolinea.getSelectedIndex() == 0 || txtFechaRegistro.getText().isEmpty() || txtContrasenia.getText().isEmpty() || txtCorreo.getText().isEmpty() || txtConfirmarContrasenia.getText().isEmpty() || !txtContrasenia.getText().equals(txtConfirmarContrasenia.getText())) {
            if(!txtContrasenia.getText().equals(txtConfirmarContrasenia.getText())){
                JOptionPane.showMessageDialog( null , "Las contraseñas NO coinciden");
                return;
            }else if(cboAerolinea.getSelectedIndex() == 0 ){
                JOptionPane.showMessageDialog(null, "Seleccione una Aerolinea para registrar al Administrador");
            }else {
                JOptionPane.showMessageDialog(null, "Existen campos obligatorios por completar");
                return;
            }
        }else {
            String nombres = txtNombres.getText();
            String apellidos = txtApellidos.getText();
            int edad = Integer.parseInt(txtEdad.getText());
            String identificacion = txtIdentificacion.getText();
            String genero = String.valueOf(cboGenero.getSelectedItem());
            String telefono = txtTelefono.getText();
            String fechaRegistro = txtFechaRegistro.getText();
            String correo = txtCorreo.getText();
            String contrasenia = txtContrasenia.getText();
            String aerolineaSeleccionada = String.valueOf(cboAerolinea.getSelectedItem());
            Aerolinea aerolinea = controlador.buscarAeroNombre(aerolineaSeleccionada);
            AdministradorAerolinea administradorAerolinea = new AdministradorAerolinea(fecha, identificacion, nombres, apellidos, edad, genero, telefono, correo, contrasenia, "Administrador Aerolinea");
            Aerolinea aerolineaGuardar = new Aerolinea(aerolinea.getNombreAerolinea(), aerolinea.getCodigoAerolinea(), aerolinea.getPais(), aerolinea.getListaAviones(), controlador.obtenerListaEmpleados(aerolinea.getCodigoAerolinea()));
            
            try{
                controlador.guardarAerolinea(aerolinea, administradorAerolinea);
                JOptionPane.showMessageDialog(null, "Se registró correctamente la aerolinea " + aerolineaGuardar.getNombreAerolinea() + "\n con Administrador de Aerolinea: " + nombres + " " + apellidos);
                limpiarCampos();
                actualizarTabla();
            }catch (CodigoAerolineaExisteException | CorreoRegistradoException | AdminAeroRegistradoException | GestorRegistradoException | CapitanVueloRegistradoException | EmpleadoLogisticaRegistradoException | InformacionViajeroException e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
    }//GEN-LAST:event_btnRegistrarMouseClicked

