    private void actualizarTabla(){
        LSE<GestorMantenimiento> gestores = controlador.obtenerGestores();
        try{
            for (int i = 0; i < 3 ; i++) {
                for (int j = 0; j < modelo.getRowCount(); j++) {
                    modelo.removeRow(j);
                }   
            }
        }catch(NullPointerException e){
        }
            try{
                for (int i = 0; i < gestores.size() ; i++) {
                    GestorMantenimiento aux = gestores.get(i);                   
                    Object[] ob = {aux.getNombres(), aux.getApellidos(), aux.getIdentificacion(), aux.getGenero(), aux.getCiudadResidencia(), aux.getCorreo()};
                    modelo.addRow(ob);                    
                }
            }catch(NullPointerException e){        
            }
        }
    
