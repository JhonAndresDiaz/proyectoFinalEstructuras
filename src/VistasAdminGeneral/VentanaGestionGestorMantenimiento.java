    private void btnEditarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEditarMouseClicked

        if(txtNombres.getText().isEmpty() || txtApellidos.getText().isEmpty() || txtIdentificacion.getText().isEmpty() || txtEdad.getText().isEmpty() || txtTelefono.getText().isEmpty() || cboGenero.getSelectedIndex() == 0 || txtEspecialidad.getText().isEmpty() || txtContrasenia.getText().isEmpty() || txtCorreo.getText().isEmpty() || txtConfirmarContrasenia.getText().isEmpty() || !txtContrasenia.getText().equals(txtConfirmarContrasenia.getText())) {
            if(!txtContrasenia.getText().equals(txtConfirmarContrasenia.getText())){
                JOptionPane.showMessageDialog( null , "Las contraseñas NO coinciden");
                return;
            }else {
                JOptionPane.showMessageDialog(null, "Existen campos obligatorios por completar");
                return;
            }
        }else {

            String nombres = txtNombres.getText();
            String apellidos = txtApellidos.getText();
            int edad = Integer.parseInt(txtEdad.getText());
            String identificacion = txtIdentificacion.getText();
            String genero = String.valueOf(cboGenero.getSelectedItem());
            String telefono = txtTelefono.getText();
            String especialidad = txtEspecialidad.getText();
            String correo = txtCorreo.getText();
            String contrasenia = txtContrasenia.getText();
            String residencia = txtCiudadResidencia.getText();
            String certificaciones = txtCertificaciones.getText();

            GestorMantenimiento gestorMantenimiento = new GestorMantenimiento(especialidad, certificaciones, residencia, identificacion, nombres, apellidos, edad, genero, telefono, correo, contrasenia, "Gestor Mantenimiento");

            try{
                controlador.editarGestor(gestorMantenimiento);
                JOptionPane.showMessageDialog(null, "La informacion del gestor de mantenimiento: " +nombres +" "+apellidos +  "\n ha sido actualizada");
                limpiarCampos();
                actualizarTabla();
            }catch(CambioCorreoIdentificacionException | IdentificacionNoExisteException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
                actualizarTabla();
            }
        }
    }//GEN-LAST:event_btnEditarMouseClicked

