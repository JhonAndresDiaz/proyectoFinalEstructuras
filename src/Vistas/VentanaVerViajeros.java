    private void actualizarTabla(){
        LSE<Viajero> aerolineas = controlador.obtenerViajerosTabla();
        try{
            for (int i = 0; i < 3 ; i++) {
                for (int j = 0; j < modelo.getRowCount(); j++) {
                    modelo.removeRow(j);
                }   
            }
        }catch(NullPointerException e){
        }
            try{
                for (int i = 0; i < aerolineas.size() ; i++) {
                    Viajero aux = aerolineas.get(i);                   
                    Object[] ob = {aux.getNombres(), aux.getApellidos(), aux.getIdentificacion(), aux.getCorreo(),  aux.getContrasenia()};
                    modelo.addRow(ob);                    
                }
            }catch(NullPointerException e){        
            }
    }
    
