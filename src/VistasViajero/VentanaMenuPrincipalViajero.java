    private void btnEditarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEditarMouseClicked
        if(txtNombres.getText().isEmpty() || txtApellidos.getText().isEmpty() || txtIdentificacion.getText().isEmpty() || txtEdad.getText().isEmpty() || txtTelefono.getText().isEmpty() || cboGenero.getSelectedIndex() == 0 || txtPaisNacimiento.getText().isEmpty() || txtContrasenia.getText().isEmpty() || txtCorreo.getText().isEmpty() || txtConfirmarContrasenia.getText().isEmpty() || !txtContrasenia.getText().equals(txtConfirmarContrasenia.getText())) {
            if(!txtContrasenia.getText().equals(txtConfirmarContrasenia.getText())){
                JOptionPane.showMessageDialog( null , "Las contraseñas NO coinciden");
                return;
            }else {
                JOptionPane.showMessageDialog(null, "Existen campos obligatorios por completar");
                return;
            }
        }else {
            String nombres = txtNombres.getText();
            String apellidos = txtApellidos.getText();
            int edad = Integer.parseInt(txtEdad.getText());
            String identificacion = txtIdentificacion.getText();
            String genero = String.valueOf(cboGenero.getSelectedItem());
            String telefono = txtTelefono.getText();
            String paisNacimiento = txtPaisNacimiento.getText();
            String correo = txtCorreo.getText();
            String contrasenia = txtContrasenia.getText();

            Viajero viajero = new Viajero(paisNacimiento, controlador.obtenerVuelos(identificacion), controlador.obtenerReservas(identificacion), identificacion, nombres, apellidos, edad, genero, telefono, correo, contrasenia, "Viajero");             

            try{
                controlador.editarViajero(viajero);
                JOptionPane.showMessageDialog(null, "La informacion del viajero: " +nombres +" "+apellidos +  "\n ha sido actualizada");
                llenarInformacion();
            }catch(IdentificacionNoExisteException e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
    }//GEN-LAST:event_btnEditarMouseClicked

